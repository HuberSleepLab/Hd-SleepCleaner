function EEG = makeEEG(data, srate)
    EEG=[];
    EEG.setname = '';
    EEG.filename = '';
    EEG.filepath = '';
    EEG.subject = '';
    EEG.group = '';
    EEG.condition = '';
    EEG.session = [];
    EEG.comments = '';
    EEG.nbchan = size(data,1);
    EEG.trials = 1;
    EEG.pnts = size(data,2);
    EEG.srate = srate;
    EEG.xmin = 0;
    EEG.xmax = single((EEG.pnts-1)/EEG.srate+EEG.xmin);
    EEG.times = single(linspace(EEG.xmin*1000, EEG.xmax*1000, EEG.pnts));
    EEG.timesSec = single(0:1/srate:EEG.xmax);
    EEG.data = single(data);
    EEG.icaact = [];
    EEG.icawinv = [];
    EEG.icasphere = [];
    EEG.icaweights = [];
    EEG.icachansind = [];
    EEG.chanlocs = [];
    EEG.urchanlocs = [];
    EEG.chaninfo = [];
    EEG.ref = [];
    EEG.event = [];
    EEG.urevent = [];
    EEG.eventdescription = {};
    EEG.epoch = [];
    EEG.epochdescription = {};
    EEG.reject = [];
    EEG.stats = [];
    EEG.specdata = [];
    EEG.specicaact = [];
    EEG.splinefile = '';
    EEG.icasplinefile = '';
    EEG.dipfit = [];
    EEG.history = '';
    EEG.saved = 'no';
    EEG.etc = [];
end